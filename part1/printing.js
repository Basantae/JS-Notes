// printing.js - Output in JavaScript

// console.log() -> prints text or variables with a new line
console.log("Hello");
console.log("Bashanta");

// process.stdout.write() -> prints text without adding a new line
process.stdout.write("Bashanta");
process.stdout.write("Rokaha");

// console.table() -> prints objects or arrays in table format
console.table({ city: "Pokhara" });

// console.warn() -> prints a warning message in the console
console.warn({ city: "Pokhara" });

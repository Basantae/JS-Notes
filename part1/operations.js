// JavaScript Operations: Definitions and Examples

// 1. Arithmetic Operations
// -----------------------
// + : addition
// - : subtraction
// * : multiplication
// / : division
// % : remainder (modulus)
// ** : exponent (power)

let score = 102;
let bonus = 25;

let totalScore = score + bonus;    // 102 + 25 = 127

let addition = 4 + 5;              // 9
let subtract = 9 - 3;              // 6
let mult = 3 * 5;                  // 15
let divi = 8 / 2;                  // 4
let remainder = 9 % 2;             // 1 (9 divided by 2 leaves 1)
let expo = 2 ** 3;                 // 8 (2 * 2 * 2)

let myScore = 110;
myScore++;                          // 111 (increment by 1)

let credits = 56;
credits--;                          // 55 (decrement by 1)

// 2. Comparison Operations
// ------------------------
// ==  : equal to
// !=  : not equal to
// >   : greater than
// <   : less than
// >=  : greater than or equal to
// <=  : less than or equal to

let num1 = 3;
let num2 = 3;
let num3 = 6;

console.log(num1 == num2);   // true, because 3 equals 3
console.log(num1 != num3);   // true, because 3 is not 6
console.log(num1 > num3);    // false, 3 is not greater than 6
console.log(num1 < num3);    // true, 3 is less than 6
